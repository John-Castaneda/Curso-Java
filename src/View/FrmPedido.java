    private void jButtonCrearClActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearClActionPerformed
        // TODO add your handling code here:
        String idCliente = jTextFieldIdClienteCl.getText();
        String numeroDocumento = jTextFieldNumeroDocumentoCl.getText();
        String nombre = jTextFieldNombreCl.getText();
        String direccion = jTextFieldDireccionCl.getText();
        String ciudad = jTextFieldCiudadCl.getText();
        String depto = jTextFieldDeptoCl.getText();
        String telefono = jTextFieldTelefonoCl.getText();
        String email = jTextFieldEmailCl.getText();
        String FechaNacimiento = jTextFieldFechaNacCl.getText();
        String fidelizacion = jComboFidelizacionCl.getSelectedItem().toString();
        String tipoDocumento = "";
        //RADIO BUTTONS
        if (jRadioButtonTipoDocumentoCcCl.isSelected())
        {
            tipoDocumento += "CC";
        } else if (jRadioButtonTipoDocumentoCeCl.isSelected())
        {
            tipoDocumento += "CE";
        } else if (jRadioButtonTipoDocumentoNitCl.isSelected())
        {
            tipoDocumento += "NIT";
        } else if (jRadioButtonTipoDocumentoOtroCl.isSelected())
        {
            tipoDocumento += "OTRO";
        }
        boolean fide = false;
        if (fidelizacion == "SI")
        {
            fide = true;
        } else
        {
            fide = false;
        }

        boolean clienteCreado = false;
        clienteCreado = ctlCliente.crear(idCliente, FechaNacimiento, 0, fide, tipoDocumento,
                numeroDocumento, nombre, direccion,
                ciudad, depto, telefono, email, listaClientes);
        if (clienteCreado)
        {
            JOptionPane.showMessageDialog(this, "El cliente se creo exitosamente.");
        }

        limpiarCamposCliente();
        refrescarListaCliente();
    }//GEN-LAST:event_jButtonCrearClActionPerformed

