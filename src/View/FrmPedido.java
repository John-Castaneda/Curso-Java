    public void refrescarTablaProducto() {
        // se crea una lista de datos para que guarde los nombres de la tabla
        String[] titulosTabla =
        {
            "id", "nombre", "descripcion", "cantidad Inventario",
            "precio unitario", "proveedor"
        };
        // se crea la matriz para que la vista consulte el controlador y 
        //le ingresa la vista productos para convertirla en una matriz string
        String[][] matrizProductos = ctlProducto.matriz();
        //llenar el modelo que se llama tabla producto con los titulos y los datos de productos
        tablaProducto.setDataVector(matrizProductos, titulosTabla);
        // el modelo se le asigna al componente de la vista llamada jtableproducto
        jTableProductoPr.setModel(tablaProducto);
    }

